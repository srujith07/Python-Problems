# Clear Dictionary
# Clear all key-value pairs from a given dictionary and print it.
#
# Given:
#
# my_dict = {'name': 'Alice', 'age': 35, 'city': 'New York'}
# Expected Output:
#
# {}

my_dict = {'name': 'Alice', 'age': 35, 'city': 'New York'}
print(f"dictionary: {my_dict}")

my_dict.clear()
print(f"dictionary after removing all items: {my_dict}")
