# Find Maximum and Minimum
# Find and print the largest and smallest number in a list [8, 2, 15, 1, 9].
#
# Given:
#
# data = [8, 2, 15, 1, 9]
# Expected Output:
#
# Largest number: 15
# Smallest number: 1

data = [8, 2, 15, 1, 9]

print(f"Largest Number: {max(data)}")
print(f"Smallest Number: {min(data)}")
